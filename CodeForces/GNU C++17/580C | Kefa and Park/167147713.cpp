vector<int> a(N) ;
vector<vector<int>>adj(N);
int n , m ;
int ans = 0 ;
void dfs(int u , int p ,int nc , int mc){
  if(a[u]){
    nc++;
  }
  else{
    nc=0;
  }
  mc = max(mc,nc);
  int nk = 0 ;
  for(int v : adj[u]){
    if(v!=p){
      dfs(v , u , nc , mc );
      nk++;
    }
  }
  if(nk==0 && mc <=m){
    ans ++ ;
  }
}
