#include<bits/stdc++.h>
using namespace std;
#define ll  long long 
const int N= 505 ;
long long mat[N][N];
int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    #ifndef ONLINE_JUDGE
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);
    #endif
    int n ; 
    cin>>n ;
    for(int i=1 ; i<=n ; i++){
      for(int j = 1 ;j<=n ; j++){
        cin>>mat[i][j] ;
      }
    }
    vector<int> del(n) ;
    for(int i=0 ; i<n ; i++){
      cin>>del[i];
    }
    vector<ll> ans ;
    reverse(del.begin() , del.end());
    for(int k = 0 ; k < n ;k++){
      for(int i = 1 ; i<= n ;i++){
        for(int j = 1 ; j <= n ; j++){
          int x = del[k] ;
          long long d = mat[i][x] + mat[x][j] ;
          mat[i][j] = min(mat[i][j],d);
        }
      }
      ll  sum = 0 ;
      for(int i = 0 ; i <= k ; i++){
        for(int j = 0 ; j<=k ; j++){
          sum +=mat[del[i]][del[j]];
        }
      } 
      ans.push_back(sum) ;
    }
    reverse(ans.begin(),ans.end());
    for(auto it : ans ){
      cout<<it<<" " ;
    }
    cout<<endl;
    
}