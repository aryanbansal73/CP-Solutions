void child(vector<int> & cnt , int p ,vector<int> adj[] , int u ){
  for(auto it : adj[u]){
    if(it!= p){
      child(cnt , u , adj , it );
      cnt[u] += cnt[it];
    }
  }
  return ;
}
void dfs(vector<int> adj[] , vector<int> &cnt , vector<int> &dp,int u , int p ){
  vector<int>  chld;
  for(auto it : adj[u]){
    if(it!=p){
      chld.push_back(it) ;
      dfs(adj , cnt , dp ,it , u );
    }
  }
  if(adj[u].size()==1) return ; 
  if(adj[u].size()==2){
    dp[u] =  cnt[chld[0]] - 1 ;
    return ;
  }
  int x = chld[0] , y = chld[1] ;
  dp[u] = max(dp[x] + cnt[y] -1 , dp[y]+cnt[x]-1);
  return ;
}
