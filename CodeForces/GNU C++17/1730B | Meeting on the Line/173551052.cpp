#include<bits/stdc++.h>
using namespace std;
#define ll  long long 
pair<double , double> intersec(pair<double , double>inter,pair<double , double>range){
  if(range.second > inter.first || range.first < inter.second){
    return  {-1 , -1} ;
  }
  return {min(inter.first , range.first) , max(inter.second , range.second)};
}
