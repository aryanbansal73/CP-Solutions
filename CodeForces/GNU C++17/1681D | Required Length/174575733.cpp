#include<bits/stdc++.h>
using namespace std;
#define ll  long long int
ll countDigits(ll n){
  ll x = 0 ; 
  while(n){
    x++;
    n/=10;
  }
  return x ;
}
int main(){
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
  #ifndef ONLINE_JUDGE
  freopen("input.txt", "r", stdin);
  freopen("output.txt", "w", stdout);
  #endif   
  ll n , x ;
  cin>>n>>x ;
  map<ll,ll> lvl ; 
  queue<ll> q ;
  lvl[x] = 0 ;
  q.push(x);
  ll ans = -1 ,  tr = 0;
  while(q.empty() == false ){
    ll k = q.front() ; 
    q.pop() ;
    if(countDigits(k)==n){
      ans = lvl[k] ;
      tr = 1 ;
      break ;
    }
    ll temp = k ;
    while(temp){
      ll d = temp%10 ;
      temp/=10 ;
      if(lvl.find(d*k)==lvl.end()){
        q.push(d*k);
        lvl[d*k] = lvl[k]+1 ;
      }
    }
  }
  cout<<ans<<endl;
}