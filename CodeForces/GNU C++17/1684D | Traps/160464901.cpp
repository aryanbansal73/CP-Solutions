#include <bits/stdc++.h>
using namespace std;
#define ll  long long
#define ull  unsigned long long
int  main(){
    int  t ;
    cin>>t;
    while(t--) {
        ll n, k;
        cin >> n >> k;
        ll a[n], b[n], c[n], q = 0;
        for (ll i = 0; i < n; i++) {
            cin >> a[i];
            q += a[i];
            b[i] = q;
            c[i] = a[i] + i - n + 1;
        }
        sort(c, c + n);
        ll u = 0;
        for (int i = n - 1; i > n -1- k; i--) {
            u += c[i];
        }
        ll ans = b[n-1] - u - (k*(k-1))/2;
        cout << ans<< endl;
    }
}
 