#include <bits/stdc++.h>
using namespace std;
#define ll long long
bool tr(map<int,int> mp , int n , int t ,int m ){
    int a  = 0, b =0 ;
    for(int  i = 1 ; i<= n ;i++){
        a+= min(t , mp[i]);
        if(mp[i]<t){
            b+= (t -mp[i])/2;
        }
    }
    return a+b >=m ;
}
int main(){
    int t ;
    cin>>t;
    while(t--){
        int n , m ;
        cin>>n>>m;
        int a[m];
        map<int, int> mp;
        for(int i=0 ; i<m ; i++){
            cin>>a[i];
            mp[a[i]]++;
        }
        int  s = 0 , x =200000;
 
        while((abs(s-x))<=10){
            int  h = (s+x)/2;
            if(tr(mp , n , h , m)){
                x= h+1;
            }
            else{
                s = h-1;
            }
        }
        for(int i =s-5 ; i <=x + 5 ; i++ ) {
            if (!tr(mp, n, i, m) && (tr(mp, n, i + 1, m))) {
                cout << i + 1 << endl;
                break;
            }
        }
    }
}