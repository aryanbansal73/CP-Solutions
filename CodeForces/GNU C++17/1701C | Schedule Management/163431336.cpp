#include <bits/stdc++.h>
using namespace std;
#define ll long long
bool tr(vector<int> v  , int n ,int t ){
    ll cost =0, remainder =0 ;
    for(int i = 1 ; i<=n ;i++){
        if(v[i]<=t){
            cost += (t-v[i])/2;
        }
        else{
            remainder += v[i] -t ;
        }
    }
    return cost >=remainder;
}
