#include<bits/stdc++.h>
using namespace std ;
#define ll  long long
int main(){
    int n ;
    cin>>n;
    unordered_map<ll , ll > xx;
    unordered_map<ll,ll > yy;
    map <pair<ll,ll>, ll> allMap ;
    for(ll i=0; i<n;i++){
        ll x , y ;
        cin>>x>>y;
        xx[x]++;
        yy[y]++;
        allMap[{x,y}]++;
    }
    ll ans = 0;
    for(auto itr : xx){
        ans+= ((itr.second-1)*itr.second)/2;
    }
    for(auto itr : yy){
        ans+= ((itr.second-1)*itr.second)/2;
    }for(auto itr : allMap){
        ans-= ((itr.second-1)*itr.second)/2;
    }
    cout<<ans<<endl;
}