using namespace std;
#define ll long long
int d[100005] = {0} , dp[100005] = {0} , p =0;
int main ()
{
    int n ;
    cin>>n;
    int a[n+1] ;
    for (int i=1 ; i<=n ;i++){
        cin>>a[i];
        for(int j = 1 ; j*j<= a[i] ; j++) {
            if(a[i]%j ==0){
                dp[i] = max(dp[i] , dp[d[a[i]/j]]+1);
                if(j>1){
                    dp[i] = max(dp[i] ,dp[d[j]]+1);
                }
                p = max(p , dp[i]);
                d[j] = d[a[i]/j] = i;
 
            }
        }
 
    }
    cout<<p<<endl;
 
}