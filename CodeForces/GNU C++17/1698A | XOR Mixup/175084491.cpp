#define ll  long long int
bool fact(int n , vector<int> a ){
  vector<ll>  b(n );
  b[0]=a[0];
  for(int i = 1 ;i<n;i++){
    b[i] = b[i-1]+a[i];
  }
  if(b[n-1]!=0) return false ;
  bool vis = 0 , ok = 1 ;
  for(int i = 0 ; i<n;i++){
    if(b[i]<0)return false ;
    else{
      if(b[i]==0) vis =1 ;
      else if(vis) ok = 0 ;
    }
  } 
  return ok ;
}
