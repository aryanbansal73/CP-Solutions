    cin>>t;
    while(t--){
        ll n ;
        cin>>n;
        ll a[n];
        ll o=0 ;
        for(int i=0 ; i<n ; i++){
            cin>>a[i];
            ll k = a[i];
            if(k%2 ==1 ){
                o++;
            }
        }
        if(o>0){
            cout<<n-o<<endl;
        }
        else if(n==1){
            int l = a[0];
            ll count = 0 ;
            while(l%2==0){
                l/=2;
                count++;
            }
            cout<<count<<endl;
        }
        else{
 
            for(int i = 0 ; i < n; i ++){
                ll d = 0 ;
                while(a[i]%2 ==0){
                    a[i]/=2;
                    d++;
                }
                a[i] = d ;
            }
            ll p = *min_element(a , a+n) ;
            cout<<(p +n-1)<<endl;
        }
    }
