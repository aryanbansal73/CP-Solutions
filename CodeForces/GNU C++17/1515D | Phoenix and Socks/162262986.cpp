#include <bits/stdc++.h>
using namespace std;
#define ll  long long
#define ull  unsigned long long
 
int  main(){
    int t ;
    cin>>t ;
    while(t--){
        ll n , l , r ;
        cin>>n>>l>>r;
        vector<ll>c(n) , a ;
        unordered_map<ll , ll> ml  , mr ;
        for(int i=0 ; i<n ; i ++){
            cin>>c[i];
            if(i<l) {
                ml[c[i]]++;
            }
            else{
                mr[c[i]]++;
            }
        }
        for(int i = 1 ; i<=n ; i++){
            ll mn = min(ml[i] , mr[i]);
            ml[i]-=mn;
            mr[i]-=mn;
            l-=mn;
            r-=mn;
        }
        if(l<r){
            swap(l,r );
            swap(ml , mr );
        }
        ll ans = 0;
        for(int i = 1 ; i<=n ; i++){
            ll x = l-r ,c = ml[i]/2;
            ll d = min(c*2  , x);
            ans +=d/2;
            l-=d;
        }
        ans += (l-r)/2 + (l+r)/2;
        cout<<ans<<endl;
 
 
    }
}