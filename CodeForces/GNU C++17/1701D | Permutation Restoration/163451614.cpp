#include <bits/stdc++.h>
using namespace std;
#define ll long long
int a[500000];
int main(){
    int t ;
    cin>>t;
    while(t--){
        int n ;
        cin>>n;
        int b[n+1] , a[n];
        b[0] = 0;
        for(int i=1;i<=n ;i++){
            cin>>b[i];
        }
        vector<vector<pair<ll,ll>>> seg(n+1);
        for(int i = 1 ; i<=n ;i++){
            int mn = (i/(b[i]+1)  +1) , mx =n;
            if(b[i]){
                mx = (i/b[i]);
            }
            seg[mn].push_back({mx, i});
        }
        set<pair<ll,ll>> s ;
        for(int i = 1 ; i<=n ;i++){
            for(auto it: seg[i]){
                s.insert(it);
            }
            a[s.begin()->second] = i;
            s.erase((s.begin()));
        }
        for(int i =1 ;i<=n;i++){
            cout<<a[i]<<" ";
        }
        cout<<endl;
 
    }
}