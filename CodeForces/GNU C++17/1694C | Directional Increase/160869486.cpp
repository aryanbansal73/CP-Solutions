#include <bits/stdc++.h>
using namespace std;
#define ll  long long
#define ull  unsigned long long
 
int  main(){
    ll t  ;
    cin>>t;
    while(t--){
        ll n ;
        cin>>n;
        vector<ll> a(n);
        bool t = true ;
        for(int i=0;i<n;i++){
            cin>>a[i];
        }
        vector<ll> v(n ,0);
        for(int i = 1 ; i< n ;i++){
            ll x = a[i-1] - v[i-1] ;
            v[i-1]+= x ;
            v[i]-=x ;
            if(x<=0){
                if(x<0) {
                    t = false;
                }
                break;
            }
        }
        for(int i=0 ; i<n;i++){
            if(a[i]!=v[i]){
                t=  false;
            }
        }
        if(t){
            cout<<"YES"<<endl;
        }
        else{
            cout<<"NO"<<endl;
        }
 
 
    }
 
 
}
 