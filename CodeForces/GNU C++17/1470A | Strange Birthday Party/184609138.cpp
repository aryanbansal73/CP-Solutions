void primefactors( vector<int> & v , int  n ){
  while( n%2 == 0 ){
    v.push_back(2) ; 
    n/=2 ;
  }
  for(int  i = 3 ; i<= sqrt(n) ; i+=2){
    while(n%i ==0){
      v.push_back(i);
      n = n/i ;
    }
  }
  if(n>2 ){
    v.push_back(n) ;
  }
}
int max_map(unordered_map<char , int> &v){
  int ans = INT_MIN ; 
  for(auto it : v){
    ans = max(ans , it.second);
  }
  return ans ;
}
