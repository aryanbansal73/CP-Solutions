#include <bits/stdc++.h>
using namespace std;
#define ll long long
int main ()
{   ll n , k ;
    cin>>n>>k;
    map<int,int> mp;
    queue<int>q;
    for(int i = 0 ;i<= 30;i++){
        if((1<<i)&n){
            mp[1<<i]++;
            if(i>0){
                q.push(1<<i);
            }
        }
    }
    if(int(mp.size()) > k)
    {
        puts("NO");
        return 0;
    }
    int cnt = mp.size();
    while(cnt < k && !q.empty())
    {
        int z = q.front();
        q.pop();
        mp[z]--;
        mp[z / 2] += 2;
        if(z / 2 > 1)
        {
            q.push(z / 2);
            q.push(z / 2);
        }
        cnt++;
    }
    if(cnt < k)
    {
        puts("NO");
        return 0;
    }
    puts("YES");
    for(auto x : mp)
        for(int i = 0; i < x.second; i++)
            printf("%d ", x.first);
    puts("");
    return 0;
 
}