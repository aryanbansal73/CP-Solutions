char find(vector<array<long long, 4>> &a, string &s, long long k) {
    if (k< 0) {
        return ' ';
    }
 
    else if (k< s.size()) {
        return s[k];
    }
    else {
        for (int i = 0; i < a.size(); i++)
        {
            if (a[i][0] <= k && a[i][1] >= k)
                return find (a, s, a[i][2]+k-a[i][0]);
        }
    }
 
    return ' ';
 
}
 
