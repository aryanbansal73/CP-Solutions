#include <bits/stdc++.h>
using namespace std;
#define ll  long long
int  le( string s , int x , int y ){
    int r = 0 ;
    for(auto it : s){
        if(it - '0' == x){
            r++;
            swap( x , y);
        }}
        if(x!=y && r%2 == 1){
            r-- ;
        }
        return r ;
}
int  main(){
   int t ;
   cin>>t ;
   while(t--){
       string s ;
       cin>>s;
       int m = INT_MIN;
       for(int i = 0 ; i < 10 ; i++){
           for(int  j = 0 ; j < 10 ; j++){
               int l = le(s , i ,j);
               m = max(m , l);
           }
       }
       int ans = s.length() - m ;
       cout<<ans<<endl;
   }
}