bool fact(){
  int n  ;
  cin>>n ; 
  vector<int> a(n) , b , c, d ; 
  unordered_map<int ,int> mp ;
  for(int i = 0 ; i< n ;i++){
    cin>>a[i] ;
    if(a[i]>0) b.push_back(a[i]);
    if(a[i]<0) c.push_back(a[i]);
    if(a[i] ==0){
      if(d.size()<2){
        d.push_back(0);
        mp[0]++;
      }
    }
  }
  if(b.size()>2 || c.size()>2){
    return false ;
  }
  for(auto it: b){
    d.push_back(it) ;
    mp[it]++;
  }
  for(auto it: c){
    d.push_back(it) ;
    mp[it]++;
  }
  int l = d.size();
  for(int i =0 ; i< l ;i++){
      for(int j =i+1 ; j< l ;j++){
        for(int k =j+1 ; k< l ;k++){
          int s = d[i]+d[j]+d[k];
          if(mp[s]>0){}
          else{
            return false;
          }
        }
    }
  }
  return true ;
}
