#include <bits/stdc++.h>
using namespace std;
#define ll  long long
#define ull  unsigned long long
 
int  main(){
    int t ;
    cin>>t;
    while(t--){
        ll n ;
        cin>>n;
        ll f = 1 , c = 1 ;
        vector<ll> fac;
        while(f<=1e12){
            fac.push_back(f);
            f*=(c++) ;
        }
        ll l = fac.size();
        ll minim = INT_MAX;
        for(int i = 0 ;i < (1<<l); i++){
            ll  s =0  , k = 0 ;
            for(int j = 0 ; j < l ; j++) {
                if(i&(1<<j)){
                    s+=fac[j];
                    k++;
                }
            }
           minim =  min( minim , k + __builtin_popcountll(n-s));
        }
        cout<<minim<<endl;
    }
}
 