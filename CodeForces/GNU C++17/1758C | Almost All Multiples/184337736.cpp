int gcd1(int a , int b ){
  return b==0?a:gcd1(b , a%b);
}
void primefactors( vector<int> & v , int  n ){
  while( n%2 == 0 ){
    v.push_back(2) ; 
    n/=2 ;
  }
  for(int  i = 3 ; i<= sqrt(n) ; i+=2){
    while(n%i ==0){
      v.push_back(i);
      n = n/i ;
    }
  }
  if(n>2 ){
    v.push_back(n) ;
  }
}
