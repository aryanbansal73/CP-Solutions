#include <bits/stdc++.h>
using namespace std;
#define ll  long long
int  main(){
   int n ;
   cin>>n;
  int x[n] , h[n] , x1[n] , x2[n];
  for(int i = 0 ; i < n ; i++){
      cin>>x[i]>>h[i];
      x1[i] = x[i] - h[i] ;
      x2[i] = x[i] + h[i] ;
  }
  int r = 2 ;
  for(int i = 1 ; i < n-1 ; i++){
      if(x[i] - x[i-1] > h[i]){
          r++;
      }
      else if(x[i+1] - x[i] > h[i]){
          r++;
          x[i]+=h[i];
      }
  }
    if(n!=1) {
        cout << r << endl;
    }
    else{
        cout<<1<<endl;
    }
}