#define ll long long
bool t(ll j , ll a[] , ll b[] , ll n , ll k){
    ll tmp = k ;
    for(int i=0 ; i<n ; i++)
    {
        ll need =  1LL * a[i] * j ;
        if(need > b[i])
            tmp -= (need - 1LL*b[i]);
        if(tmp<0)
            return false;
    }
    return true ;
}
