#include<bits/stdc++.h>
using namespace std;
#define ll  long long 
const int N = 1001;
vector<int> wt(N , N) ;
void pps(){
  wt[1] = 0 ; 
  for(int i = 1 ; i<N ; i++){
    for(int j = 1 ; j<=i;j++){
      int ni = i + i/j ;
      if(ni <N){
        wt[ni] = min(wt[ni], wt[i]+1) ;
      }
    }
  }
}
