#include <bits/stdc++.h>
using namespace std;
#define ll long long
int f [10000001] , a [10000001] ,b[10000001];
 
int main(){
    string s ;
    cin >> s;
    stack<int> stk ;
    int n = s.size(), r =0;
    for(int i = 0; i < n ;i++){
        char c = s[i] ;
        if(c=='('){
            stk.push(i);
        }
        else{
            if(stk.empty() ){
                a[i] = b[i] = -1 ;
            }
            else{
                int x = stk.top();
                stk.pop();
                a[i] = b[i] = x;
                if(x>0 && s[x-1]==')'&& b[x-1]>=0){
                    a[i] = a[x-1];
                }
                int l = i - a[i] +1 ;
                f[l]++;
                r = max(r , l) ;
            }
 
        }
    }
    f[0] = 1;
    cout<<r<<" "<<f[r]<<endl;
}