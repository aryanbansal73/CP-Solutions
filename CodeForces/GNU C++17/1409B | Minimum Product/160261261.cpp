#include <bits/stdc++.h>
using namespace std;
#define ll  long long
int  main(){
   int t ;
   cin>>t ;
   while(t--){
 
       ll a , b , x , y , n ;
       cin>>a>>b>>x>>y>>n;
       ll m , l ;
       m = max(x , a-n)*max(b- n + min(n , a-x) , y);
       l = max(y, b-n)*max(a- n + min(n ,b-y) , x);
       ll ans = min(m , l );
       cout<<ans<<endl;
   }
}